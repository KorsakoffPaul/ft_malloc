#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int long	m;

	m = n;
	if (m < 0)
	{
		ft_putchar_fd('-', fd);
		m *= -1;
	}
	if (m >= 10)
		ft_putnbr_fd(m / 10, fd);
	m %= 10;
	ft_putchar_fd(m + '0', fd);
}
